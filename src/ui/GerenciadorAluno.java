    private void btnPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarActionPerformed
        // TODO add your handling code here:
         int entradaMatricula = Integer.parseInt(matricula.getText());
        if (comboBoxAdministrador.getSelectedItem().equals("Disciplina")) {            
            try {    
                retorno.setText(ds.buscarDisciplina(entradaMatricula).toString());
            } catch (ExceptionElementoInvalido ex) {
                retorno.setText(ex.getMessage());
            }
        }else if (comboBoxAdministrador.getSelectedItem().equals("Turma")) {
            try {
                retorno.setText(trm.buscarTurma(entradaMatricula).toString());
            } catch (ExceptionElementoInvalido ex) {
                retorno.setText(ex.getMessage());
            }
        }else if (comboBoxAdministrador.getSelectedItem().equals("Professor")) {                
            try {
               retorno.setText(iprof.buscarProfessor(entradaMatricula).toString());
            } catch (ExceptionElementoInvalido ex) {
               retorno.setText(ex.getMessage());
            }       
        }else if (comboBoxAdministrador.getSelectedItem().equals("Aluno")) {                        
            try {
               retorno.setText(alun.buscarAluno(entradaMatricula).toString());
            } catch (ExceptionElementoInvalido ex) {
               retorno.setText(ex.getMessage());
            }          
        }

    }//GEN-LAST:event_btnPesquisarActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        int entradaRendimento = Integer.parseInt(idRendimento.getText());
        int entradaTurma = Integer.parseInt(idTurma.getText());
        int entradaAluno = Integer.parseInt(idAluno.getText());
        Turma armazenaTurma = null;
        Aluno armazenaAluno = null;
        for (Turma turma : trm.listarTurma()) {
            if (turma.getIdTurma() == entradaTurma) {
                armazenaTurma = turma;
            }
        }
        for (Aluno aluno : alun.listarAluno()) {
            if (aluno.getMatricula() == entradaAluno) {
                armazenaAluno = aluno;
            }
        }
        
        String[] t = {trab1.getText(), trab2.getText(),  trab3.getText(), trab4.getText()};
        RendimentoEscolar rendimentoEscolar = new RendimentoEscolar(entradaRendimento, armazenaTurma, armazenaAluno, null, null, t, null, 0.0);     
        if (rendimentoEscolar != null) {
        rd.atribuir(rendimentoEscolar);
        retorno.setText("Cadastro efetuado com sucesso!");
        }else{
        retorno.setText("Erro");
        }
       
    }//GEN-LAST:event_jButton3ActionPerformed

